    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $user = User::find($id);
        if($user == null){
            return redirect('/user');
        }
        $success = $user->delete();
        if(!$success){
            return response()->json(['error' => 'error while deleting']);
        }
        return redirect('/user');
    }
